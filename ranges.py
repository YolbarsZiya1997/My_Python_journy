for i in range(10, 16):
    print("i is now {}".format(i))
    # the last value classified is not included
    
for i in range(10):
    print("i is now {}".format(i))

for i in range(10,16,2):
    # the -2 in the end is the step value
    print("i is now {}".format(i))
    
for i in range(10,0,-2):
    # usage of negative indexs
    print("i is now {}".format(i))
    
